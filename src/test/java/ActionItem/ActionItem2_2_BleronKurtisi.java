package ActionItem;

public class ActionItem2_2_BleronKurtisi {
    public static void main(String[] args) {

        //declare  and define the variable for myGrade
       String myGrade = "G";

        switch (myGrade){
            case "A":
                System.out.println("Excellent");
                break;
            case "B":
                System.out.println("Well done");
                break;
            case "C":
                System.out.println("Satisfactory");
                break;
            case "D":
                System.out.println("Passed");
                break;
            case "F":
                System.out.println("Fail");
                break;
            default:
                System.out.println("Invalid Grade");
        }


    }//end of main
}//end of java class
